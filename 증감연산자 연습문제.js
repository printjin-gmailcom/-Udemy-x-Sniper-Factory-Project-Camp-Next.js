// 1. 변수 `y`에 숫자 7을 할당하세요.
let y = 7;

// 2. 후위 증가 연산자를 사용하여 `y`를 1 증가시키고, 증가하기 전과 후의 값을 각각 출력하세요.
console.log("후위 증가 전:", y); // 증가하기 전 값 출력
console.log("후위 증가 후:", y++); // 증가 전 값 출력 후 증가
console.log("후위 증가 결과:", y); // 증가 후 값 출력

// 3. 후위 감소 연산자를 사용하여 `y`를 1 감소시키고, 감소하기 전과 후의 값을 각각 출력하세요.
console.log("후위 감소 전:", y); // 감소하기 전 값 출력
console.log("후위 감소 후:", y--); // 감소 전 값 출력 후 감소
console.log("후위 감소 결과:", y); // 감소 후 값 출력

// 4. 전위 증가 연산자를 사용하여 `y`를 1 증가시키고, 증가한 값을 출력하세요.
console.log("전위 증가 결과:", ++y); // 증가 후 값 출력

// 5. 전위 감소 연산자를 사용하여 `y`를 1 감소시키고, 감소한 값을 출력하세요.
console.log("전위 감소 결과:", --y); // 감소 후 값 출력
