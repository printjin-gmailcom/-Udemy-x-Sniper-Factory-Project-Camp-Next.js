// 1. 변수 선언과 할당
const x = 10;
const y = 20;
const sum = x + y;
console.log("Sum:", sum);

// 2. 변수 재할당
let name = "Alice"; // let을 사용하여 재할당 가능하게 선언
name = "Bob";
console.log("Name:", name);

// 3. 상수
const PI = 3.14;
console.log("PI:", PI);

// 4. 변수 스왑
let a = 5; // let을 사용하여 재할당 가능하게 선언
let b = 10;

// 변수를 서로 교환
const temp = a;
a = b;
b = temp;

console.log("a:", a);
console.log("b:", b);
